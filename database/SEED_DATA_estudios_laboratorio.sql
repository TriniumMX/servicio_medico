-- Solución al error: "no hay restricción única o de exclusión que coincida con ON CONFLICT"
-- Este script realiza 3 pasos OBLIGATORIOS para que funcione la inserción:
-- 1. Elimina duplicados existentes en la base de datos para limpiar la tabla.
-- 2. Crea la restricción UNIQUE en 'nombre_estudio' (requisito para usar ON CONFLICT).
-- 3. Inserta o Actualiza los estudios con la información de la lista.

BEGIN;

-- PASO 1: Eliminar duplicados exactos (dejando el más reciente por ID)
DELETE FROM public.cat_estudios_laboratorio
WHERE id_estudio IN (
    SELECT id_estudio
    FROM (
        SELECT id_estudio,
               ROW_NUMBER() OVER (partition BY nombre_estudio ORDER BY id_estudio DESC) as rn
        FROM public.cat_estudios_laboratorio
    ) t
    WHERE rn > 1
);

-- PASO 2: Crear Índice Único (Si no existe)
-- Esto previene futuros duplicados y habilita la función de "Actualizar si existe"
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_class c
        JOIN pg_namespace n ON n.oid = c.relnamespace
        WHERE c.relname = 'uk_cat_estudios_nombre' AND n.nspname = 'public'
    ) THEN
        ALTER TABLE public.cat_estudios_laboratorio
        ADD CONSTRAINT uk_cat_estudios_nombre UNIQUE (nombre_estudio);
    END IF;
END $$;

